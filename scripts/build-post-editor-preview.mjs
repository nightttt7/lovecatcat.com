import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { build } from "esbuild";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(scriptDir, "..");
const entryPoint = path.join(rootDir, "src", "markdown", "browser-preview.ts");
const outputFile = path.join(rootDir, "src", "assets", "post-editor-preview.generated.ts");

const result = await build({
  entryPoints: [entryPoint],
  bundle: true,
  format: "iife",
  minify: false,
  platform: "browser",
  target: ["es2020"],
  write: false
});

const bundledFile = result.outputFiles?.[0];

if (!bundledFile) {
  throw new Error("Failed to generate post editor preview bundle.");
}

const generatedModule = [
  "// This file is generated by npm run build:assets. Do not edit manually.",
  `export const postEditorPreviewScript = ${JSON.stringify(bundledFile.text)};`,
  ""
].join("\n");

await mkdir(path.dirname(outputFile), { recursive: true });
await writeFile(outputFile, generatedModule, "utf8");

console.log(`Wrote ${path.relative(rootDir, outputFile)}`);